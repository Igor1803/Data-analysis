import pandas as pd
import numpy as np

# Создаем словарь с данными
data = {
    'Ученик': ['Иванов И.', 'Петров П.', 'Сидоров С.', 'Козлова К.', 'Морозов М.', 
               'Волкова В.', 'Соколов С.', 'Павлова П.', 'Никитин Н.', 'Орлова О.'],
    'Математика': [5, 4, 3, 5, 4, 3, 5, 4, 4, 5],
    'Русский язык': [4, 5, 4, 5, 3, 4, 4, 5, 3, 4],
    'Физика': [5, 4, 3, 4, 5, 3, 4, 4, 5, 3],
    'История': [3, 5, 4, 5, 4, 5, 3, 4, 4, 5],
    'Литература': [4, 4, 5, 3, 4, 5, 4, 5, 3, 4]
}

# Создаем датафрейм
df = pd.DataFrame(data)

# 1. Вывод первых строк
print("Первые 5 строк DataFrame:")
print(df.head())
print("\n" + "="*50 + "\n")

# 2. Средние оценки по предметам
print("Средние оценки по предметам:")
print(df.mean(numeric_only=True))
print("\n" + "="*50 + "\n")

# 3. Медианные оценки по предметам
print("Медианные оценки по предметам:")
print(df.median(numeric_only=True))
print("\n" + "="*50 + "\n")

# 4. Квантили для математики
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"Q1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")
print(f"IQR по математике: {IQR_math}")
print("\n" + "="*50 + "\n")

# 5. Стандартное отклонение
print("Стандартное отклонение по предметам:")
print(df.std(numeric_only=True))